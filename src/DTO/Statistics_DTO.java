package DTO;

public class Statistics_DTO {
	private int maSP;
	private String tenSP;
	private Long doanhThu;
	private int slNhapVao;
	private int slBanRa;
	private Long loiNhuan;
	
	public Statistics_DTO() {
	}

	public int getSlNhapVao() {
		return slNhapVao;
	}

	public void setSlNhapVao(int slNhapVao) {
		this.slNhapVao = slNhapVao;
	}

	public int getSlBanRa() {
		return slBanRa;
	}

	public void setSlBanRa(int slBanRa) {
		this.slBanRa = slBanRa;
	}

	public int getMaSP() {
		return maSP;
	}

	public void setMaSP(int maSP) {
		this.maSP = maSP;
	}

	public String getTenSP() {
		return tenSP;
	}

	public void setTenSP(String tenSP) {
		this.tenSP = tenSP;
	}

	public Long getDoanhThu() {
		return doanhThu;
	}

	public void setDoanhThu(Long doanhThu) {
		this.doanhThu = doanhThu;
	}

	public Long getLoiNhuan() {
		return loiNhuan;
	}

	public void setLoiNhuan(Long loiNhuan) {
		this.loiNhuan = loiNhuan;
	}
	
	
}
